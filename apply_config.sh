echo ""

# –ü–æ–∏—Å–∫ –∫–æ–Ω—Ç–µ–π–Ω–µ—Ä–∞ Marzban
CONTAINER_NAME=$(docker ps --format "{{.Names}}" | grep -i marzban | head -n 1)

