# –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: ./apply_config.sh [–ø—É—Ç—å_–∫_–∫–æ–Ω—Ñ–∏–≥—É.json]
# –¢—Ä–µ–±—É–µ—Ç –¥–æ—Å—Ç—É–ø–∞ –∫ —Ñ–∞–π–ª–∞–º –∫–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–∏ Marzban

